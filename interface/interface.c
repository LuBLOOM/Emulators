static void 
interface_init(void)
{
	unsigned int i;

	SDL_Init(SDL_INIT_VIDEO);

	window = SDL_CreateWindow("Welcome to Interface", SDL_WINDOWPOS_UNDEFINED,
		SDL_WINDOWPOS_UNDEFINED, INTERFACE_WIDTH, INTERFACE_HEIGHT,
		SDL_WINDOW_SHOWN);

	renderer = SDL_CreateRenderer(window, -1, 0);

	maxsize = 1024;

	#ifdef __CHIP__

	chip_access_off = SDL_CreateTexture(renderer, 0, 0, CHIP_BITMAP_WIDTH, CHIP_BITMAP_HEIGHT);
	chip_access_on = SDL_CreateTexture(renderer, 0, 0, CHIP_BITMAP_WIDTH, CHIP_BITMAP_HEIGHT);

	chip_dest.w = CHIP_BITMAP_WIDTH;
	chip_dest.h = CHIP_BITMAP_HEIGHT;
	chip_dest.x = (INTERFACE_WIDTH-chip_dest.w)/2;
	chip_dest.y = (INTERFACE_HEIGHT-chip_dest.h)/2;
	unsigned int chip_bitmap_pixels_on[0x1000] = {0};
	unsigned int chip_bitmap_pixels_off[0x1000] = {0};
	for (i = 0; i < 0x1000; i++) {
		unsigned int pixel = chip_bitmap[i];
		chip_bitmap_pixels_off[i] = (0x00ffffff * pixel);
		chip_bitmap_pixels_on[i] = ~chip_bitmap_pixels_off[i];
		chip_bitmap_pixels_off[i] |= 0xff000000;
		chip_bitmap_pixels_on[i] |= 0x00ff0000;
	}
	SDL_UpdateTexture(chip_access_off, NULL, chip_bitmap_pixels_off, 64 * sizeof(unsigned int));
	SDL_UpdateTexture(chip_access_on, NULL, chip_bitmap_pixels_on, 64 * sizeof(unsigned int));

	#endif
}

static void
interface_render(void)
{

	bool interface_running = true;
	SDL_Event event;

	while (interface_running) {
		while (SDL_PollEvent(&event)) {
			if (event.type == SDL_QUIT) {
				interface_running = false;
			}
			#if __CHIP__
			if (event.motion.x > chip_dest.x && event.motion.x < chip_dest.x + chip_dest.w 
				&& event.motion.y > chip_dest.y && event.motion.y < chip_dest.y + chip_dest.h) {
				chip_hovered = true;
			} else {
				chip_hovered = false;
			}
			if (event.type == SDL_MOUSEBUTTONDOWN 
				&& event.button.button == SDL_BUTTON_LEFT && chip_hovered) {
				FILE *zenity_fd = popen("zenity --file-selection", "r");
				fgets(romname, maxsize, zenity_fd);
				fclose(zenity_fd);
				_entrypoint_emu_chip();
			}
			#endif
		}
		SDL_SetRenderDrawColor(renderer, 0x00, 0x00, 0x00, 0xff);
		SDL_RenderClear(renderer);
		#ifdef __CHIP__
		if (chip_hovered) {
			SDL_RenderCopy(renderer, chip_access_on, NULL, &chip_dest);
		} else {
			SDL_RenderCopy(renderer, chip_access_off, NULL, &chip_dest);
		}
		#endif
		SDL_RenderPresent(renderer);
	}
}

static void 
interface_free(void)
{
	SDL_DestroyWindow(window);
	SDL_DestroyRenderer(renderer);
	SDL_Quit();
}

void 
_entrypoint_interface(void)
{
	interface_init();
	interface_render();
	interface_free();
}



